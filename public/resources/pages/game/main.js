// ***************************************************** WIN EFFECT
function winEffect() {
    var winShow = document.querySelector('#win');
    winShow.classList.toggle('show01')
    console.log('you win')
}
// ***************************************************** LOSE EFFECT
function loseEffect() {
    var loseShow = document.querySelector('#lose');
    loseShow.classList.toggle('show02')
    console.log('you lose')
}



function draw() {
// ***************************************************** STOP GAME - YOU LOSE
    if(!lives){
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        drawBricks();
        drawBall();
        drawPaddle();
        drawScore();
        drawLives();
        loseEffect();
        
    }
// ***************************************************** STOP GAME - YOU WIN    
    else if(score == 15){
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        drawBricks();
        drawBall();
        drawPaddle();
        drawScore();
        drawLives();
        winEffect();
    }
    else {

    
// ***************************************************** CONTINUE GAME 
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    drawBricks();
    drawBall();
    drawPaddle();
    drawScore();
    drawLives();
    collisionDetection();
    
    if(x + dx > canvas.width-ballRadius || x + dx < ballRadius) {
        dx = -dx;
    }
    if(y + dy < ballRadius) {
        dy = -dy;
    }
    else if(y + dy > canvas.height-ballRadius) {
        if(x > paddleX && x < paddleX + paddleWidth) {
            dy = -dy;
            // PAD SOUND
            gameAudio.audio.pad.play();
        }
        else {
            let die = false;
            lives--;
            if(lives != 0){
                // LOSE HEALTH SOUND
                gameAudio.audio.loseHealth.play();
            }
            if(!lives) {
                // GAME OVER SOUND
                gameAudio.audio.gameOver.play();
                console.log("GAME OVER");
            }
            else {
                x = canvas.width/2;
                y = canvas.height-30;
                dx = 3;
                dy = -3;
                paddleX = (canvas.width-paddleWidth)/2;             
            }
        }
    }
    
    if(rightPressed && paddleX < canvas.width-paddleWidth) {
        paddleX += 7;
    }
    else if(leftPressed && paddleX > 0) {
        paddleX -= 7;
    }
    
    x += dx;
    y += dy;
    requestAnimationFrame(draw);
}
}
